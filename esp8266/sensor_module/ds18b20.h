#ifndef DS18B20_H
#define DS18B20_H

#include <Arduino.h>

// Инициализация датчика
void ds18b20_begin();

// Запрос измерения (неблокирующий — занимает ~750мс)
void ds18b20_requestTemperature();

// Чтение результата (вызывать через 750мс после запроса)
// Возвращает температуру в °C или -127.0 если датчик не найден
float ds18b20_getTemperature();

// Проверка готовности результата (прошло ли достаточно времени)
bool ds18b20_isReady();

#endif
